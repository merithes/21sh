#include "hmini.h"

void				move_back(int i)
{
	write(1, "\033[", 2);
	ft_putnbr(i);
	write(1, "D", 1);
}

void				move_forw(int i)
{
	write(1, "\033[", 2);
	ft_putnbr(i);
	write(1, "C", 1);
}
